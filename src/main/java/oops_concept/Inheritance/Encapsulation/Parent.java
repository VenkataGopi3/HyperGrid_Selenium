package oops_concept.Inheritance.Encapsulation;

public class Parent {

    private int id;
    private String name;
    private double no;

    public void setId(int id){
         this.id = id;

    }
    public int getId()
    {
        return id;
    }

    public void setName(String name){
        this.name = name;
    }
    public String getName()
    {
        return name;
    }

    public void setNo(double no){
        this.no = no;

    }
    public double getNo()
    {
        return no;
    }
}
