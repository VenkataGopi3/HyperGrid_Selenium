package Arrays;

public class TwoDimensionsArray {
    public static void main(String[] args) {

        int[][] k ={{10,3,14,8,333},{9,45,26,56,7},{2,3,4,5,6,},{100,122,124},{1111,1234,12345}};

        System.out.println(k[0][2]);
        System.out.println(k[1][3]);
        System.out.println(k[0][1]);
        System.out.println(k[1][4]);
        System.out.println(k[3][1]);
        System.out.println(k[4][2]);
        //System.out.println(k[2][1]);
    }
}
